# 通过比较运算符定义
# == ！= < > <= >=
# 演示内容的相等比较
num1 = 10
num2 = 10
print(f"10 == 10的结果是{num1 == num2}")
num1 = 10
num2 = 15
print(f"10 ！= 15的结果是{num1 != num2}")
name1 = "ysj"
name2 = "lzn"
print(f"name1 == name2的结果是{name1 == name2}")
# 演示大于小于，大于等于，小于等于的比较运算
num1 = 10
num2 = 15
print(f"10>15的结果是：{num1 > num2}")
print(f"10<15的结果是：{num1 < num2}")

num1 = 10
num2 = 10
print(f"10 >= 10的结果是：{num1 >= num2}")
print(f"10 <= 10的结果是：{num1 <= num2}")